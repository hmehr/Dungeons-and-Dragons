package Controls.CustomListView;

import java.awt.datatransfer.Transferable;
import Model.Map.MapCell;
import javax.swing.JComponent;
import javax.swing.JList;
import javax.swing.TransferHandler;

/**
 *The handler class for transferring the list view items.
 * 
 * @author eric
 */
public class ListViewTransferHandler extends TransferHandler
{

    /**
     * Default constructor. Calls the super type constructor.
     */
    public ListViewTransferHandler()
    {
        super();
    }

    /**
     * Returns the source of action.
     * 
     * @param pComponent The component to be passed.
     * @return An <code>int</code> representing a source action capability of either
     * &quot;copy&quot; or &quot;move&quot;.
     */
    @Override
    public int getSourceActions(JComponent pComponent)
    {
        return COPY_OR_MOVE;
    }

    /**
     * Creates transferable objects.
     * 
     * @param pComponent The component to be passed.
     * @return A transferable object.
     */
    @Override
    public Transferable createTransferable(JComponent pComponent)
    {
        // TaskInstancePanel implements Transferable
        Transferable ret = null;
        if (pComponent instanceof JList)
        {
            JList list = (JList)pComponent;
            if(list.getSelectedValue() instanceof MapCell)
            {
                ret = (Transferable)list.getSelectedValue();
            }
        }
        return ret;
    }
}
