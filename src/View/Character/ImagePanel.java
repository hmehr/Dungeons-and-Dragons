package View.Character;

import java.awt.Dimension;
import java.awt.Graphics;
import java.awt.Image;
import javax.swing.ImageIcon;
import javax.swing.JPanel;

/**
 * It's an image panel class to display an image in a JPanel.
 * @author m_niza
 */
class ImagePanel extends JPanel
{

    private Image img;

    /**
     * Constructor initiates with the path file of the image.
     * @param img It takes the path of the image file.
     */
    public ImagePanel(String img)
    {
        this(new ImageIcon(img).getImage());
    }

    /**
     * Set the size of the image panel .
     * @param img Takes the image object created from the file.
     */
    public ImagePanel(Image img)
    {
        this.img = img;
        Dimension size = new Dimension(img.getWidth(null), img.getHeight(null));
        setPreferredSize(size);
        setMinimumSize(size);
        setMaximumSize(size);
        setSize(size);
        setLayout(null);
    }

    /**
     * Implements the paint component to draw the picture of the image on the panel.
     * @param g Graphics object.
     */
    @Override
    public void paintComponent(Graphics g)
    {
        g.drawImage(img, 0, 0, null);
    }
}
