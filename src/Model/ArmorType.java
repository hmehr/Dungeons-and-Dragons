package Model;

/**
 * Enumeration of different armor types available for fighter.
 * @author Hamid
 * added new armor types for build 2
 * @version 1.2.0
 */
public enum ArmorType
{
    /**
     * This is armor type - padded.
     */
    PADDED, 
    
    /**
     * This is armor type - leather.
     */
    LEATHER,

    /**
     * This is armor type - studded leather.
     */    
    STUDDEDLEATHER,

    /**
     * This is armor type - chain shirt.
     */
    CHAINSHIRT,
    
    /**
     * This is armor type - breast plate.
     */
    BREASTPLATE,

    /**
     * This is armor type - banded mail.
     */
    BANDEDMAIL,

    /**
     * This is armor type - half plate.
     */    
    HALFPLATE,

    /**
     * This is armor type - full plate.
     */
    FULLPLATE,

    /**
     * This is armor type - light.
     */    
    LIGHT,

    /**
     * This is armor type - medium.
     */
    MEDIUM,

    /**
     * This is armor type - heavy.
     */    
    HEAVY;
}
